<script>
import CodeMirror from '@shell/components/CodeMirror';
import AppModal from '@shell/components/AppModal.vue';

export default {
  emits: ['closed'],

  components: { AppModal, CodeMirror },

  data() {

    console.log(this);

    const mode = 'edit';

    const codeMirrorOptions = {
      readOnly: mode === 'view',
      gutters: ['CodeMirror-foldgutter'],
      mode:            'shell',
      lint:            false,
      lineNumbers:     mode !== 'view',
      styleActiveLine: false,
      tabSize:         2,
      indentWithTabs:  false,
      cursorBlinkRate: 530,
    };

    return {
      data:                   '',
      currentVersion:         '',
      defaultRegistrySetting: null,
      plugin:                 undefined,
      busy:                   false,
      version:                '',
      update:                 false,
      mode:                   '',
      showModal:              false,
      chartVersionInfo:       null,
      codeMirrorOptions,
    };
  },

  methods: {
    showDialog(data) {
      this.showModal = true;
      this.data = data;
    },

    closeDialog(result) {
      this.showModal = false;

      this.$emit('closed', {
        success: result,
        value: this.data,
      });
    },
  }
};
</script>

<template>
  <app-modal
    v-if="showModal"
    name="sshKnownHostsDialog"
    height="auto"
    :scrollable="true"
    @close="closeDialog(false)"
  >
    <div
      class="ssh-known-hosts-dialog"
    >
      <h4 class="mt-10">
        {{ t('secret.ssh.editKnownHosts.title') }}
      </h4>
      <div class="custom mt-10">
        <div class="dialog-panel">
          <CodeMirror
            :value="data"
            class="editor"
            :options="codeMirrorOptions"
            :showKeyMapBox="true"
            @onInput="data=$event"
          />
        </div>
        <div class="dialog-buttons">
          <button
            :disabled="busy"
            class="btn role-secondary"
            data-testid="ssh-known-hosts-dialog-cancel-btn"
            @click="closeDialog(false)"
          >
            {{ t('generic.cancel') }}
          </button>
          <button
            class="btn role-primary"
            data-testid="ssh-known-hosts-dialog-save-btn"
            @click="closeDialog(true)"
          >
            {{ t('generic.save') }}
          </button>
        </div>
      </div>
    </div>
  </app-modal>
</template>

